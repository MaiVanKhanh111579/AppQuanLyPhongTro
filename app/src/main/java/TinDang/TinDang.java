package TinDang;

public class TinDang {
    public int imageRes;
    public String tieuDe;
    public String gia;
    public String diaChi;
    public String zalo;

    public TinDang(int imageRes, String tieuDe, String gia, String diaChi, String zalo) {
        this.imageRes = imageRes;
        this.tieuDe = tieuDe;
        this.gia = gia;
        this.diaChi = diaChi;
        this.zalo = zalo;
    }
}
